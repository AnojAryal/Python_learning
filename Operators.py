# Addition
a = 5
b = 7
c = a + b
print("The sum of", a, "and", b, "is", c)

# Subtraction
d = 12
e = 5
f = d - e
print("The difference between", d, "and", e, "is", f)

# Multiplication
g = 6
h = 4
i = g * h
print("The product of", g, "and", h, "is", i)

# Division
j = 20
k = 4
l = j / k
print("The quotient of", j, "and", k, "is", l)

# Modulus
m = 17
n = 3
o = m % n
print("The remainder when", m, "is divided by", n, "is", o)

# Exponentiation
p = 2
q = 3
r = p ** q
print(p, "raised to the power of", q, "is", r)
